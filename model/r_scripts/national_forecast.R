casename  <- "india_no_int"        
        
model_3 <- tvt.eSIR(
  Y,
  R,
  begin_str      = format(min_date, "%m/%d/%Y"),
  death_in_R     = 0.2,
  T_fin          = 200,
  R0             = R_0,
  dic            = TRUE,
  casename       = casename,
  save_files     = save_files,
  save_mcmc      = save_mcmc,
  save_plot_data = save_plot_data,
  M              = Ms,
  nburnin        = nburnins
)
        
